"""Universal section renderers — emit on every (or near-every) node type.

These section renderers don't belong to any single node type; per-type
``render_body_X`` functions in sibling modules compose them alongside
their type-specific sections.

  - ``render_description``               — every node has a Description.
  - ``render_associated_nodes``          — every node carries the
                                           Associated Nodes stub
                                           rewritten post-build by
                                           ``scripts/build/associate.py``.
  - ``render_source_form_notes``         — every node may carry
                                           ``naming_quirks`` with
                                           ``resolution: preserve-as-sic-
                                           in-quotes``.
  - ``render_preserved_disagreements``   — every node may carry
                                           ``naming_quirks`` with
                                           ``resolution: disputed``.
  - ``render_primary_source_contradictions``
                                         — emits on every type whose
                                           schema permits ``rumors``
                                           (person / organization /
                                           event / location). Filters
                                           rumors by status
                                           ``primary-source-disputed``.
  - ``render_public_record_claims``      — parallel surface to
                                           ``render_primary_source_contradictions``
                                           filtering rumors by status
                                           ``not-primary-source-established``.
                                           Same four target types.
  - ``render_timeline``                  — person / event / organization
                                           share a single Timeline shape.
  - ``render_corroboration``             — eyewitness persons + encounter
                                           events share a single
                                           Corroboration shape.
  - ``render_provenance``                 — document (gov-doc) + media
                                           share a single Provenance
                                           shape (custody chain table
                                           from ``context_extrinsic
                                           .provenance``).
"""

from ._common import _escape_table_cell, _wrap_path, sort_by_date


def render_description(artifact):
    desc = (artifact.get("description") or "").strip()
    body = desc if desc else "<!-- TODO: populate `description` in the research artifact -->"
    return f"## Description\n\n{body}\n"


def render_associated_nodes():
    """Placeholder — ``scripts/build/associate.py`` rewrites this section
    post-build by scanning body links. We emit a stub so the section
    exists for ``associate.py`` to find."""
    return (
        "## Associated Nodes\n\n"
        "<!-- Auto-generated by scripts/build/associate.py. Do not hand-edit. -->\n"
        "<!-- placeholder — associate.py rewrites this section post-build -->\n"
    )


def render_source_form_notes(artifact):
    """Emit a `## Source-Form Notes` section listing `naming_quirks`
    entries whose resolution is `preserve-as-sic-in-quotes` —
    contributor-registered source-form preservations (auto-caption
    typos, OCR artifacts, alias-of-record forms) that appear verbatim
    in quote text and would otherwise read to a reader as contributor
    misspellings without explicit signal.

    Returns empty string when no preserve-as-sic entries exist.
    `disputed` resolutions render via render_preserved_disagreements
    (parallel section); `use-canonical` and `unresolved` stay
    contributor-side metadata only.

    Applies to every node type — naming_quirks is universal across
    target types.
    """
    quirks = artifact.get("naming_quirks") or []
    preserved = [
        q for q in quirks
        if isinstance(q, dict) and q.get("resolution") == "preserve-as-sic-in-quotes"
    ]
    if not preserved:
        return ""

    lines = [
        "## Source-Form Notes",
        "",
        "Source-form preservations — verbatim tokens in quote text "
        "that the cited primary source attests in a non-canonical form "
        "(auto-caption typos, OCR artifacts, alias-of-record). The "
        "verbatim form is preserved in quoted text per source-read-"
        "first discipline; the canonical form is recorded here for "
        "navigation.",
        "",
        "| Source Form | Canonical | Source | Note |",
        "|---|---|---|---|",
    ]
    for q in preserved:
        observed = _escape_table_cell(q.get("observed"))
        canonical = _escape_table_cell(q.get("canonical"))
        source_path = _escape_table_cell(q.get("source_path"))
        note = _escape_table_cell(q.get("note"))
        lines.append(f"| {observed} | {canonical} | {source_path} | {note} |")
    return "\n".join(lines) + "\n"


def render_preserved_disagreements(artifact):
    """Emit a `## Preserved Disagreements` section listing
    `naming_quirks` entries whose resolution is `disputed` — two
    primary sources attest opposing forms of the same fact and the
    contributor has chosen not to adjudicate.

    Returns empty string when no disputed entries exist."""
    quirks = artifact.get("naming_quirks") or []
    disputed = [
        q for q in quirks
        if isinstance(q, dict) and q.get("resolution") == "disputed"
    ]
    if not disputed:
        return ""

    lines = [
        "## Preserved Disagreements",
        "",
        "Naming-quirk entries where two primary sources attest opposing "
        "forms of the same fact. The repository does not adjudicate; "
        "both positions stay on the record.",
        "",
        "| Position | Counterpart | Source | Note |",
        "|---|---|---|---|",
    ]
    for q in disputed:
        observed = _escape_table_cell(q.get("observed"))
        canonical = _escape_table_cell(q.get("canonical"))
        source_path = _escape_table_cell(q.get("source_path"))
        note = _escape_table_cell(q.get("note"))
        lines.append(f"| {observed} | {canonical} | {source_path} | {note} |")
    return "\n".join(lines) + "\n"


def render_primary_source_contradictions(artifact):
    """Emit a `## Primary-Source Contradictions` section listing rumors
    whose status is `primary-source-disputed` — widely-circulated
    secondary-source claims that primary-source evidence refutes.

    Returns empty string when no disputed rumors exist. Disputed
    rumors are analytical findings worth surfacing to readers; the
    parallel `not-primary-source-established` status renders via
    ``render_public_record_claims`` (separate section so readers can
    distinguish active refutation from absence-of-primary-source).

    Applies to the four target types whose schema rules require
    `rumors` (person / organization / event / location). Document /
    transcript / media artifacts don't carry rumors and never emit
    this section."""
    rumors = artifact.get("rumors") or []
    disputed = [
        r for r in rumors
        if isinstance(r, dict) and r.get("status") == "primary-source-disputed"
    ]
    if not disputed:
        return ""

    parts = [
        "## Primary-Source Contradictions",
        "",
        "Widely-circulated secondary-source claims contradicted by primary-source evidence.",
        "",
    ]
    for r in disputed:
        claim = (r.get("claim") or "").strip()
        if not claim:
            continue
        parts.append(f'### "{claim}"')
        parts.append("")
        obs = r.get("observed_sources")
        if obs:
            if isinstance(obs, list):
                obs_str = "; ".join(str(o) for o in obs)
            else:
                obs_str = str(obs)
            parts.append(f"**Circulates in:** {obs_str}")
            parts.append("")
        note = (r.get("note") or "").strip()
        if note:
            parts.append(f"**Primary-source refutation:** {note}")
            parts.append("")
    return "\n".join(parts).rstrip() + "\n"


def render_public_record_claims(artifact):
    """Emit a `## Public-Record Claims Without Primary Source` section
    listing rumors whose status is `not-primary-source-established` —
    widely-reported claims circulating in public discourse that the
    repository has not yet archived a primary source for.

    Parallel surface to ``render_primary_source_contradictions``
    (which renders the active-refutation case). This renderer
    surfaces the absence-of-primary-source case so readers see both
    confirmed content and contributor-curated unsourced material on
    the same node — closing the prior asymmetry where disputed
    rumors rendered but not-established rumors stayed artifact-only.

    The schema's fabrication-prevention purpose is preserved: rumors
    are still investigator memory, but they're now reader-visible
    investigator memory. A reader landing on the node sees the
    repository's stance ("we know this circulates; we haven't sourced
    it") rather than silent omission.

    Returns empty string when no qualifying rumors exist. Applies to
    the four target types whose schema rules require `rumors`
    (person / organization / event / location)."""
    rumors = artifact.get("rumors") or []
    qualifying = [
        r for r in rumors
        if isinstance(r, dict) and r.get("status") == "not-primary-source-established"
    ]
    if not qualifying:
        return ""

    parts = [
        "## Public-Record Claims Without Primary Source",
        "",
        "Widely-reported claims that circulate in public discourse but lack primary-source backing in this repository's archive. Contributor-curated; treat as leads, not facts.",
        "",
    ]
    for r in qualifying:
        claim = (r.get("claim") or "").strip()
        if not claim:
            continue
        parts.append(f'### "{claim}"')
        parts.append("")
        obs = r.get("observed_sources")
        if obs:
            if isinstance(obs, list):
                obs_str = "; ".join(str(o) for o in obs)
            else:
                obs_str = str(obs)
            parts.append(f"**Circulates in:** {obs_str}")
            parts.append("")
        note = (r.get("note") or "").strip()
        if note:
            parts.append(f"**Investigator note:** {note}")
            parts.append("")
    return "\n".join(parts).rstrip() + "\n"


def render_timeline(artifact):
    """Timeline section — aggregated chronological dated facts with
    Category column. Sorted ascending by date. Shared between person,
    event, and organization renderers."""
    items = sort_by_date(
        [e for e in (artifact.get("timeline") or []) if isinstance(e, dict)],
        "date",
    )
    lines = ["## Timeline", "", "| Date | Event | Category | Source | Node Link |",
             "|---|---|---|---|---|"]
    if not items:
        lines.append("|  |  |  |  |  |")
    for e in items:
        lines.append(
            f"| {e.get('date') or ''} | "
            f"{e.get('event') or ''} | "
            f"{e.get('category') or ''} | "
            f"{(e.get('source') or {}).get('path') or ''} | "
            f"{_wrap_path(e.get('node_link'))} |"
        )
    return "\n".join(lines) + "\n"


def render_provenance(artifact):
    """Custody-chain table from ``context_extrinsic.provenance``. Emits
    on document (gov-doc) and media nodes — both attest a chain of
    custody for their primary source. Columns: Step | Date | Entity |
    Verified."""
    ctx = artifact.get("context_extrinsic") or {}
    prov = ctx.get("provenance")
    lines = ["## Provenance", ""]
    if not isinstance(prov, list) or not prov:
        lines.append("<!-- TODO: populate `context_extrinsic.provenance` in the research artifact -->")
        return "\n".join(lines) + "\n"
    lines.append("| Step | Date | Entity | Verified |")
    lines.append("|---|---|---|---|")
    for step in prov:
        if not isinstance(step, dict):
            continue
        lines.append(
            f"| {step.get('step', '')} | {step.get('date', '')} | "
            f"{step.get('entity', '')} | {step.get('verified', '')} |"
        )
    return "\n".join(lines) + "\n"


def render_corroboration(artifact):
    """Corroboration section — renders on eyewitness person artifacts AND
    encounter event artifacts (same entry shape, same renderer). Column
    layout: Observer first (what the investigator is looking for),
    Attested In last (the source documenting the corroboration).
    """
    items = artifact.get("corroboration_items") or []
    lines = ["## Corroboration", "",
             "| Observer | Type | What It Confirms | Attested In |",
             "|---|---|---|---|"]
    if not items:
        lines.append("|  |  |  |  |")
    for e in items:
        if not isinstance(e, dict):
            continue
        lines.append(
            f"| {_wrap_path(e.get('observer_path'))} | "
            f"{e.get('observation_type') or ''} | "
            f"{e.get('note') or ''} | "
            f"{(e.get('source') or {}).get('path') or ''} |"
        )
    return "\n".join(lines) + "\n"
